#!/usr/bin/env node
/**
 * generate-alias-redirects.mjs — build /_redirects with query-intent
 * URL aliases for every state × product.
 *
 *   node scripts/generate-alias-redirects.mjs
 *
 * Rationale: Netlify placeholders (`:name`) only match FULL URL
 * segments, so a pattern like `/lender-in-:state/` doesn't work
 * (`:state` is embedded inside a segment). Enumerating explicit
 * per-state redirects is more reliable and Netlify handles thousands
 * of static redirect rules without issue.
 *
 * Aliases generated for each state:
 *   /lender-in-<state>/                → /rental/<state>/          (DSCR — highest-intent)
 *   /private-lender-in-<state>/        → /rental/<state>/
 *   /dscr-lender-in-<state>/           → /rental/<state>/
 *   /dscr-loans-<state>/               → /rental/<state>/
 *   /hard-money-lender-in-<state>/     → /fix-n-flip/<state>/
 *   /fix-and-flip-lender-in-<state>/   → /fix-n-flip/<state>/
 *   /fix-and-flip-loans-<state>/       → /fix-n-flip/<state>/
 *   /construction-lender-in-<state>/   → /new-construction/<state>/
 *   /new-construction-loans-<state>/   → /new-construction/<state>/
 *
 * Purpose: inbound-link magnets. If someone links to
 * slacapital.ai/hard-money-lender-in-texas/ from a REI forum, the
 * link authority flows to /fix-n-flip/texas/ via 301. Google indexes
 * the destination — no duplicate-content risk on the alias URL itself.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const STATES_PATH = path.join(REPO_ROOT, 'data', 'states.json');
const OUT = path.join(REPO_ROOT, '_redirects');

const { states } = JSON.parse(fs.readFileSync(STATES_PATH, 'utf8'));

const ALIASES = [
  { pattern: 'lender-in-{state}/',                to: 'rental' },
  { pattern: 'private-lender-in-{state}/',        to: 'rental' },
  { pattern: 'dscr-lender-in-{state}/',           to: 'rental' },
  { pattern: 'dscr-loans-{state}/',               to: 'rental' },
  { pattern: 'hard-money-lender-in-{state}/',     to: 'fix-n-flip' },
  { pattern: 'fix-and-flip-lender-in-{state}/',   to: 'fix-n-flip' },
  { pattern: 'fix-and-flip-loans-{state}/',       to: 'fix-n-flip' },
  { pattern: 'construction-lender-in-{state}/',   to: 'new-construction' },
  { pattern: 'new-construction-loans-{state}/',   to: 'new-construction' },
];

const lines = [];
lines.push('# Generated by scripts/generate-alias-redirects.mjs — do NOT hand-edit.');
lines.push('# Query-intent URL aliases → canonical state product pages (301).');
lines.push('# Rerun the script after editing data/states.json or the ALIAS list.');
lines.push('');

let count = 0;
for (const alias of ALIASES) {
  lines.push(`# ─── ${alias.pattern.replace('{state}/', '')}<state>/  →  /${alias.to}/<state>/`);
  for (const state of states) {
    const from = '/' + alias.pattern.replace('{state}', state.slug);
    const to   = '/' + alias.to + '/' + state.slug + '/';
    // Column-aligned so it's readable if someone opens the file.
    lines.push(`${from.padEnd(58)} ${to.padEnd(40)} 301!`);
    count++;
  }
  lines.push('');
}

fs.writeFileSync(OUT, lines.join('\n') + '\n');
console.log(`✓ _redirects written with ${count} alias entries (${ALIASES.length} patterns × ${states.length} states)`);
